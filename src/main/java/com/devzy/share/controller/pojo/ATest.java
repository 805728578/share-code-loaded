package com.devzy.share.controller.pojo;

public class ATest {
	private int id;
	private String name;
	public ATest(int id) {
		super();
		this.id = id;
	}

	public ATest() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
